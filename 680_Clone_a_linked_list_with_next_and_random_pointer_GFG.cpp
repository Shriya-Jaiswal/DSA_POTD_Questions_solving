class Node{
    public:
        int data;
        Node* next;
        Node* random;
    Node(int data){
        this -> data = data;
        this-> next = NULL;
        this-> random = NULL;
    }

    ~Node(){
        cout << "Node with value: " << this->data << " deleted" << endl;
    }
};


