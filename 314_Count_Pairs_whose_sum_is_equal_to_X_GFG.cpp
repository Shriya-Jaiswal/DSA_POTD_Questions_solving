int countPairs(struct Node* head1, struct Node* head2, int x) {
    set<int>st;
    Node* temp=head1;
    while(temp){
        st.insert(temp->data);
        temp=temp->next;
    }
        
    int ans=0;
    temp=head2;
    while(temp){
        int val=temp->data;
        if(st.find(x-val)!=st.end()){
            ans++;
        }
        temp=temp->next;
    }
    return ans;
}

// ⏲️ Expected Time Complexity: O(length(head1)+lenght(head2)).
// 🛢️ Expected Auxiliary Space: O(length(head1)) or O(length(head2)).

int main()
{
    Node* head1 = NULL;
    Node* tail1 = NULL;
    insertAtTail(head1,tail1,1);
    insertAtTail(head1,tail1,2);
    insertAtTail(head1,tail1,3);
    insertAtTail(head1,tail1,4);
    insertAtTail(head1,tail1,5);
    insertAtTail(head1,tail1,6);
    Node* head2 = NULL;
    Node* tail2 = NULL;
    insertAtTail(head2,tail2,11);
    insertAtTail(head2,tail2,12);
    insertAtTail(head2,tail2,13);

    int x = 15;
    int ans = countPairs(head1,head2,x);
    cout<< "ans is : " << ans << endl;
    return 0;
}
