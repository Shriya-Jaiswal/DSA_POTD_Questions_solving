Node* reverse(Node* head) {
    Node *curr=head->next, *prev=head, *nxt=NULL;
    while(curr!=head){
        nxt=curr->next;
        curr->next=prev;
        prev=curr;
        curr=nxt;
    }
    head->next=prev;
    return prev;
}

