// ⏲️ Time Complexity : O(n)
// 🛢️ Space Complexity : O(n)

Node* modifiedList(vector<int>& nums, Node* head) {
    unordered_set<int> st(begin(nums), end(nums));
    Node* prev = NULL;
    Node* curr = head;


    while (curr != NULL && st.count(curr->data)) {
        head = curr->next;
        curr = head;
    }

    while (curr != NULL) {
        int currVal = curr->data;
        if (!st.count(currVal)) {
            prev = curr;
            curr = curr->next;
        } else {
            prev->next = curr->next;
            curr = curr->next;
        }
    }
    return head;
}


