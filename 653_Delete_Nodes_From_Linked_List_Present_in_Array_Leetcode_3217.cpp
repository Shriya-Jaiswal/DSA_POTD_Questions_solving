{
    Node* head = NULL;
    Node* tail = NULL;

    insertAtTail(head,tail,1);
    insertAtTail(head,tail,2);
    insertAtTail(head,tail,3);
    insertAtTail(head,tail,4);
    insertAtTail(head,tail,5);

    vector<int> nums = {1,2,3};
    Node* ans = modifiedList(nums,head);
    print(ans);

    return 0;
}
