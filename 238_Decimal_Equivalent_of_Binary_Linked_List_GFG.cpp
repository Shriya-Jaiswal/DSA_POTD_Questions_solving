// ğŸ‘‰ insertAtTail
// I want to insert a node right at the end of LINKED LIST
void insertAtTail(Node* &head , Node* &tail, int data){

    // ğŸ‘‡ firstNode
    // â¡ï¸ check for empty Ll
    if(head == NULL){
        //  LL --> Node empty hogi, and first node add hogi
        Node* newNode = new Node(data);
        head = newNode;
        tail = newNode;
        return;
    }

    // ğŸ‘‰ step create a node
    Node* newNode = new Node(data);

    tail -> next = newNode;
    
    // ğŸ‘‰ step 3
    tail = newNode;

}



long long unsigned int decimalValue(Node *head)
{
    int n=0;
    string str="";
    int mod=1e9+7;
    Node* temp=head;
    long long unsigned int power=1;
    long long unsigned int ans=0;
    while(temp){
        n++;
        str+=char(temp->data);
        temp=temp->next;
    }
    for(int i=n-1;i>=0;i--){
        int x=int(str[i]);
        ans= ans+x*power;
        ans=ans%mod;
        power= (power*2)%mod;
    }
    return ans%mod;
}


