// 👉 insertAtTail
// I want to insert a node right at the end of LINKED LIST
void insertAtTail(Node* &head , Node* &tail, int data){

    // 👇 firstNode
    // ➡️ check for empty Ll
    if(head == NULL){
        //  LL --> Node empty hogi, and first node add hogi
        Node* newNode = new Node(data);
        head = newNode;
        tail = newNode;
        return;
    }

    // 👉 step create a node
    Node* newNode = new Node(data);

    tail -> next = newNode;
    
    // 👉 step 3
    tail = newNode;

}



long long unsigned int decimalValue(Node *head)
{
    int n=0;
    string str="";
    int mod=1e9+7;
    Node* temp=head;
    long long unsigned int power=1;
    long long unsigned int ans=0;
    while(temp){
        n++;
        str+=char(temp->data);
        temp=temp->next;
    }
    for(int i=n-1;i>=0;i--){
        int x=int(str[i]);
        ans= ans+x*power;
        ans=ans%mod;
        power= (power*2)%mod;
    }
    return ans%mod;
}


// ⏲️ Expected Time Complexity: O(n)
// 🛢️ Expected Auxiliary Space: O(1)

int main()
{
    Node* head = NULL;
    Node* tail = NULL;
    insertAtTail(head,tail,1);
    insertAtTail(head,tail,1);
    insertAtTail(head,tail,1);
    insertAtTail(head,tail,0);
    
    long long unsigned int ans = decimalValue(head);
    cout << "ans is : " << ans << endl;

    return 0;
}
